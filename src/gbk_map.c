static char *gbkmap_share_name					= "HYPIM_GBKMAP_SHARED_NAME";

/**	GENERNATE GBKMAP到内存。
 *	返回：
 *		成功：1
 *		失败：0
 */
int GenGBKMapData()
{
	int i,j,pos_offset;
	byte pos_mask,*pos;

	if (share_segment->gbkmap_loaded)
		return 1;

	gbk_map = AllocateSharedMemory(gbkmap_share_name, GBKMAP_SIZE);
	if (!gbk_map)
		return 0;
	
	for ( i=0; i < sizeof(GBK_SCOPE)/sizeof(int)-1; i+=2 )
	{
		for ( j=GBK_SCOPE[i]; j <= GBK_SCOPE[i+1]; j++)
		{
			pos_offset = j >> 3;
			pos_mask = 1 << (j % 8);

			pos = (byte*)(gbk_map + pos_offset);
			*pos = (*pos) | pos_mask;			
		}
	}

	share_segment->gbkmap_loaded = 1;

	return 1;
}

/**	释放gbk MAP文件
 */
int FreeGBKMapData()
{
	share_segment->gbkmap_loaded = 0;

	if (gbk_map)
	{
		FreeSharedMemory(gbkmap_share_name, gbk_map);
		gbk_map = 0;
	}

	return 1;
}

int IsGBK(UC zi)
{
	int pos_offset;
	byte pos_mask,*pos;
	extern int GenGBKMapResource();

	if(!gbk_map)
	{
		gbk_map = GetSharedMemory(gbkmap_share_name);
		
		//可能存在其他进程已经装载了，但是退出后共享内存被释放的问题
		if (!gbk_map)
		{
			share_segment->gbkmap_loaded = 0;
			GenGBKMapResource();
		}
	}
	if(!gbk_map)
		return 1;

	if (zi > 0xFFFFF)
		return 0;

	////PUA
	//if ( !_tcscmp(pim_config->chinese_font_name,TEXT("微软雅黑")) && zi >= 0xE000 && zi <= 0xF8FF )
	//	return 0;

	pos_offset = zi >> 3;
	pos_mask = 1 << (zi % 8);

	pos = (byte*)(gbk_map + pos_offset);

	return (*pos) & pos_mask;
} 
